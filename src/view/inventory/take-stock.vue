<script>
  import TakeStockEdit from '@/components/inventory/take-stock/take-stock-edit'
  import SelectShop from '@/components/form/select-shop'
  import TakeStockDetails from '@/components/inventory/take-stock/take-stock-details'

  export default {
    name: 'TakeStock',
    components: { TakeStockDetails, SelectShop, TakeStockEdit },
    data: function () {
      return {
        loading: false,
        tableHeight: 0,
        tableData: [
          {
            0: '测试测试',
          },
        ],
        tableColumns: [
          {
            title: '盘点单据',
            key: '0',
          },
          {
            title: '仓库/门店',
            key: '1',
          },
          {
            title: '经办人',
            key: '2',
          },
          {
            title: '盘点时间',
            key: '3',
          },
          {
            title: '盘点商品种类',
            key: '4',
          },
          {
            title: '盈亏数量',
            key: '5',
          },
          {
            title: '盈亏金额',
            key: '6',
          },
          {
            title: '制单人',
            key: '7',
          },
        ],
        modal: {
          show: false,
          title: '',
          type: false,
          width: 1000,
          scrollable: true,
          styles: { top: '50px' },
        },
        searchData: {
          sid: '',
          search: '',
          start: '',
          end: '',
          p: 1,
          page: 10,
          count: 0,
        },
      }
    },
    computed: {
      modalShow() {
        return (name) => {
          return this.modal.type === name && this.modal.show
        }
      },
    },
    activated() {},
    created() {},
    mounted() {
      this.setHeight()
      window.onresize = () => {
        this.setHeight()
      }
    },
    methods: {
      setHeight() {
        this.tableHeight = window.innerHeight - 234
      },
      showModal(title, type, data = {}) {
        console.log(data)
        this.modal.show = true
        this.modal.title = title
        this.modal.type = type
        this.modal.width = window.innerWidth - 200
      },

      clearDate() {
        this.searchData.start = ''
        this.searchData.end = ''
        this.search()
      },
      changeDatePicker: function (date) {
        if (date) {
          this.searchData.start = date[0]
          this.searchData.end = date[1]
        }
        this.search()
      },
      currentPage(current) {
        this.searchData.p = current
        this.getList()
      },
      pageSizeChange(pageSize) {
        this.searchData.page = pageSize
        this.search()
      },
      search() {
        this.searchData.p = 1
        this.getList()
      },
    },
  }
</script>

